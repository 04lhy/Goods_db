    id INT PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    location VARCHAR(200) NOT NULL,
    capacity INT NOT NULL,
    status VARCHAR(20) DEFAULT 'active'
)"

$A -e "CREATE TABLE products (
    id INT PRIMARY KEY,
    name VARCHAR(200) NOT NULL,
    sku VARCHAR(50) UNIQUE NOT NULL,
    category VARCHAR(50) NOT NULL,
    unit_price DECIMAL(10,2) NOT NULL
)"

$A -e "CREATE TABLE inventory (
    warehouse_id INT NOT NULL,
    product_id INT NOT NULL,
    quantity INT DEFAULT 0,
    shelf_location VARCHAR(50),
    last_updated TIMESTAMP,
    PRIMARY KEY (warehouse_id, product_id)
)"

$A -e "CREATE TABLE customers (
    id INT PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    address VARCHAR(300),
    phone VARCHAR(20),
    membership_level VARCHAR(20) DEFAULT 'normal'
)"

$A -e "CREATE TABLE orders (
    id INT PRIMARY KEY,
    customer_id INT NOT NULL,
    order_date TIMESTAMP,
    status VARCHAR(20) DEFAULT 'pending',
    total_amount DECIMAL(12,2) NOT NULL
)"

$A -e "CREATE TABLE order_items (
    order_id INT NOT NULL,
    product_id INT NOT NULL,
    quantity INT NOT NULL,
    unit_price DECIMAL(10,2) NOT NULL,
    PRIMARY KEY (order_id, product_id)
)"

$A -e "CREATE TABLE shipments (
    id INT PRIMARY KEY,
    order_id INT NOT NULL,
    warehouse_id INT NOT NULL,
    status VARCHAR(20) DEFAULT 'processing',
    tracking_no VARCHAR(50),
    ship_date TIMESTAMP,
    arrive_date TIMESTAMP
)"

